SELECT 
    c.name as category_name,
    c.color,
    COUNT(s.id) as services_count,
    SUM(CASE WHEN s.popular = 1 THEN 1 ELSE 0 END) as popular_count
FROM categories c
LEFT JOIN services s ON c.id = s.category_id
GROUP BY c.id
ORDER BY c.id;

-- إحصائيات عامة
SELECT 
    (SELECT COUNT(*) FROM services) as total_services,
    (SELECT COUNT(*) FROM client_requests) as total_requests,
    (SELECT COUNT(*) FROM categories) as total_categories,
    (SELECT COUNT(*) FROM services WHERE status = 'active') as active_services,
    (SELECT COUNT(*) FROM client_requests WHERE status = 'new') as new_requests;

